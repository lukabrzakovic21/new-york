    public void sendSimpleMessage(EmailBuilder email) throws MessagingException {

        var messageHelper = new MimeMessageHelper(emailSender.createMimeMessage(), multipart, default_charset);
        messageHelper.setFrom(MAIL_SENDER);
        messageHelper.setTo(email.getTo());
        messageHelper.setSubject(email.getSubject());
        messageHelper.setText(email.getText(), true);

        emailSender.send(messageHelper.getMimeMessage());
    }

